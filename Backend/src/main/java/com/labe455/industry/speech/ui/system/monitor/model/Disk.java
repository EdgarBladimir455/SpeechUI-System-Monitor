package com.labe455.industry.speech.ui.system.monitor.model;

public class Disk {

	private double size;
	private double inUse;
	private double free;
	public double getSize() {
		return size;
	}
	public void setSize(double size) {
		this.size = size;
	}
	public double getInUse() {
		return inUse;
	}
	public void setInUse(double inUse) {
		this.inUse = inUse;
	}
	public double getFree() {
		return free;
	}
	public void setFree(double free) {
		this.free = free;
	}	
}
